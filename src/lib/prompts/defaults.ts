    const revealAt = (i, N) => {
      if (i <= 0) return 0; // 入场
      const k = cueIndexForStep[i];
      const r = (M > 0 && k != null && k < M)
        ? cues[k] - LEAD
        : entranceEnd + (D * 0.8 - entranceEnd) * (i / (N - 1)); // 兜底
      return Math.max(entranceEnd, Math.min(D - 12, r));
    };
